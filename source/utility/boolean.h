#ifndef BOOLEAN_H

#define BOOLEAN_H

/**
 * Type booléen.
 */
typedef unsigned char boolean;

/**
 * Valeur booléenne faux, égale à 0.
 */
#define false 0

/**
 * Valeur booléenne faux, égale à 0.
 */
#define FALSE 0

/**
 * Valeur booléenne vrai, égale à 1.
 */
#define true 1

/**
 * Valeur booléenne vrai, égale à 1.
 */
#define TRUE 1

#endif
